package com.pluralsight;

public class VariableApp {
    public static void main(String[] args) {
        String favoriteColor;
        int yearStarted;
        char middleInitial;
        boolean hasPets;
        String niceMessage;

        favoriteColor = "pink";
        yearStarted = 2025;
        middleInitial = 'E';
        hasPets = false;
        niceMessage = "Don't give up!";

        System.out.println("1. The answer is " + favoriteColor);
        System.out.println("2. The answer is " + yearStarted);
        System.out.println("3. The answer is " + middleInitial);
        System.out.println("4. The answer is " + hasPets);
        System.out.println("5. The answer is " + niceMessage);
    }
}
